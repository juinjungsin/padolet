/** 차원·난이도로 문항 조회 */
export function findQuestion(dimension: string, difficulty: string): Question {
  const q = QUESTIONS.find(
    (q) => q.dimension === dimension && q.difficulty === difficulty
  );
  if (!q) throw new Error(`문항 없음: ${dimension}/${difficulty}`);
  return q;
}
